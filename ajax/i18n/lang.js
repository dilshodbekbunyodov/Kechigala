document.addEventListener("DOMContentLoaded", () => {
    document
        .querySelectorAll("[data-i18n-key]")
        .forEach(translateElement);
});

function translateElement(element) {
    const key = element.getAttribute("data-i18n-key");
    const translation = translations[locale][key];
    element.innerText = translation;
}

function translateInputPlaceholder(element) {
    const key = element.getAttribute("data-i18n-placeholder");
    const translation = translations[locale][key];
    element.placeholder = translation;
}


function getLang(value) {
    if (value === 'ru') {
        document.querySelectorAll('#uz-button').forEach(el => {
            el.style.display = 'none';
        });
        document.querySelectorAll('#ru-button').forEach(el => {
            el.style.display = 'none';
        });
        document.querySelectorAll('#en-button').forEach(el => {
            el.style.display = 'inline-block';
        });

        locale = 'ru';
    }
    if (value === 'en') {
        document.querySelectorAll('#uz-button').forEach(el => {
            el.style.display = 'inline-block';
        });
        document.querySelectorAll('#ru-button').forEach(el => {
            el.style.display = 'none';
        })
        document.querySelectorAll('#en-button').forEach(el => {
            el.style.display = 'none';
        })

        locale = 'en';
    }
    if (value === 'uz') {
        document.querySelectorAll('#uz-button').forEach(el => {
            el.style.display = 'none';
        })
        document.querySelectorAll('#ru-button').forEach(el => {
            el.style.display = 'inline-block';
        })
        document.querySelectorAll('#en-button').forEach(el => {
            el.style.display = 'none';
        })

        locale = 'uz';
    }
    document.querySelectorAll("[data-i18n-key]").forEach(translateElement);
    document.querySelectorAll("[data-i18n-placeholder]").forEach(translateInputPlaceholder);
    console.log(`%c === ${locale} === `, `color: blue`)
}

